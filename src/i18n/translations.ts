export type TranslationKey = keyof typeof translations.id

export function t(locale: Locale, key: TranslationKey): string {
  return translations[locale][key]
}

export function tf(
  locale: Locale,
  key: TranslationKey,
  vars: Record<string, string | number>,
): string {
  return Object.entries(vars).reduce(
    (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
    t(locale, key),
  )
}

export function statusLabel(locale: Locale, status: string): string {
  const normalized =
    status === 'overload'
      ? 'perfect'
      : status === 'ok'
        ? 'low'
        : status
  if (
    normalized === 'perfect' ||
    normalized === 'good' ||
    normalized === 'low' ||
    normalized === 'bad' ||
    normalized === 'disconnected'
  ) {
    return t(locale, `status_${normalized}` as TranslationKey)
  }
  return String(status)
}

/** Baca locale aktif dari localStorage (untuk util di luar React). */
export function readStoredLocale(): Locale {
  try {
    const saved = localStorage.getItem('fo-locale')
    if (saved === 'en' || saved === 'id') return saved
  } catch {
    /* ignore */
  }
  return 'id'
}

const HANDLE_LABEL_KEYS: Record<string, TranslationKey> = {
  top: 'portTop',
  left: 'portLeft',
  right: 'portRight',
  bottom: 'portBottom',
  in: 'inputLeft',
  'in-top': 'inputTop',
  'in-right': 'inputRight',
  'in-bottom': 'inputBottom',
  probe: 'probeOpmLeft',
  'probe-top': 'probeOpmTop',
  'probe-right': 'probeOpmRight',
  'probe-bottom': 'probeOpmBottom',
  out: 'outputLabel',
  'out-top': 'outputTop',
  'out-left': 'outputLeft',
  'out-right': 'outputRight',
  'out-bottom': 'outputBottom',
  'out-small': 'branchSmall',
  'out-large': 'branchLarge',
}

/** Label port/handle untuk panel properti koneksi. */
export function handleLabel(locale: Locale, handle: string | null | undefined): string {
  if (!handle) return '—'
  const fixed = HANDLE_LABEL_KEYS[handle]
  if (fixed) return t(locale, fixed)
  if (handle.startsWith('port-')) return tf(locale, 'portN', { n: handle.slice(5) })
  if (handle.startsWith('out-')) return tf(locale, 'outN', { n: handle.slice(4) })
  if (handle.startsWith('in-')) return tf(locale, 'inputN', { n: handle.slice(3) })
  if (handle.startsWith('probe-')) return tf(locale, 'probeOpmN', { n: handle.slice(6) })
  return handle
}
